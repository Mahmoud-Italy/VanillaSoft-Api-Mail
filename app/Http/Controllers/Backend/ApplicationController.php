    public function index()
    {
        $data = Application::get();
        $rows = ApplicationResource::collection(Application::fetchData(request()->all()));
        return response()->json([
            'statusBar'   => $this->statusBar($data),
            // 'permissions' => $this->permissions('applications'),
            'rows'        => $rows,
            'paginate'    => $this->paginate($rows)
        ], 200);
    }

    public function store(ApplicationStoreRequest $request)
    {
        $row = Application::createOrUpdate(NULL, $request->all());
        if($row === true) {
            return response()->json(['message' => ''], 201);
        } else {
            return response()->json(['message' => 'Unable to create entry, ' . $row], 500);
        }
    }

    public function show($id)
    {
        $row = new ApplicationResource(Application::findOrFail(decrypt($id)));
        return response()->json(['row' => $row], 200);
    }

    public function update(ApplicationUpdateRequest $request, $id)
    {
        $row = Application::createOrUpdate(decrypt($id), $request->all());
        if($row === true) {
            return response()->json(['message' => ''], 200);
        } else {
            return response()->json(['message' => 'Unable to update entry, ' . $row], 500);
        }
    }

    public function destroy($id)
    {
        try {
            $row = Application::query();

            if(strpos($id, ',') !== false) {
                foreach(explode(',',$id) as $sid) {
                    $ids[] = $sid;
                }
                $row->whereIN('id', $ids);
            } else {
                $row->where('id', $id);
            }   
            $row->delete();

            return response()->json(['message' => ''], 200);
        } catch (\Exception $e) {
            return response()->json(['message' => 'Unable to delete entry, '. $e->getMessage()], 500);
        }
    }

